package model;

public class LeituraTWC {

    private String bico;
    private String tipoCombustivel;
    private String veiculo;
    private String odometro;
    private String frentista;
    private String odometroAnterior;
    private String motorista;

    public String getBico() {
        return bico;
    }

    public void setBico(String bico) {
        this.bico = bico;
    }

    public String getTipoCombustivel() {
        return tipoCombustivel;
    }

    public void setTipoCombustivel(String tipoCombustivel) {
        this.tipoCombustivel = tipoCombustivel;
    }

    public String getVeiculo() {
        return veiculo;
    }

    public void setVeiculo(String veiculo) {
        this.veiculo = veiculo;
    }

    public String getOdometro() {
        return odometro;
    }

    public void setOdometro(String odometro) {
        this.odometro = odometro;
    }

    public String getFrentista() {
        return frentista;
    }

    public void setFrentista(String frentista) {
        this.frentista = frentista;
    }

    public String getOdometroAnterior() {
        return odometroAnterior;
    }

    public void setOdometroAnterior(String odometroAnterior) {
        this.odometroAnterior = odometroAnterior;
    }

    public String getMotorista() {
        return motorista;
    }

    public void setMotorista(String motorista) {
        this.motorista = motorista;
    }
}
